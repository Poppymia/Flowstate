/**
 * Get color for course based on course code prefix
 */
fun getCourseColor(courseCode: String): Color {
    return when (courseCode.take(4).uppercase()) {
        "PROG" -> CoursePROG
        "INFO" -> CourseINFO
        "PSYC" -> CoursePSYC
        "MATH" -> CourseMATH
        "DESI" -> CourseDESIGN
        else -> CardPink
    }
}

/**
 * Get color for priority level (0 = low, 1 = medium, 2 = high)
 */
fun getPriorityColor(priority: Int): Color {
    return when (priority) {
        0 -> PriorityLow
        1 -> PriorityMedium
        2 -> PriorityHigh
        else -> PriorityMedium
    }
}

/**
 * Get color for task/assignment status
 */
fun getStatusColor(isCompleted: Boolean, isOverdue: Boolean): Color {
    return when {
        isCompleted -> StatusCompleted
        isOverdue -> StatusOverdue
        else -> StatusPending
    }
}

/**
 * Get appropriate text color for a given background color
 * Returns dark text for light backgrounds, light text for dark backgrounds
 */
fun getTextColorFor(backgroundColor: Color): Color {
    // Simple luminance check - could be made more sophisticated
    val luminance = (0.299 * backgroundColor.red +
            0.587 * backgroundColor.green +
            0.114 * backgroundColor.blue)
    return if (luminance > 0.5) TextPrimary else TextOnDark
}